#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*aux;
	size_t			i;

	aux = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		aux[i] = 0;
		i++;
	}
}
